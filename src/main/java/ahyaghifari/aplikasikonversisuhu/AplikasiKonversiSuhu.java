    //  METHOD CONVERT SUHU  
    public double convertSuhu(double inputSuhu, String konversiDari, String konversiKe){
        
        double hasil = 0;
        
        // CELSIUS        
        if((konversiDari == "Celsius") && konversiKe == "Fahrenheit"){
            hasil = inputSuhu * 1.8 + 32;
        }else if(konversiDari == "Celsius" && konversiKe == "Kelvin"){
            hasil = inputSuhu + 273.15;
        }else if(konversiDari == "Celsius" && konversiKe == "Reamur"){
            hasil = inputSuhu * 0.8;
        }
        
        // FAHRENHEIT
        if((konversiDari == "Fahrenheit") && konversiKe == "Celsius"){
            hasil = (inputSuhu - 32) / 1.8;
        }else if(konversiDari == "Fahrenheit" && konversiKe == "Kelvin"){
            hasil = (inputSuhu + 459.67) / 1.8;
        }else if(konversiDari == "Fahrenheit" && konversiKe == "Reamur"){
            hasil = (inputSuhu - 32) / 2.25;
        }
        
        // KELVIN
        if((konversiDari == "Kelvin") && konversiKe == "Celsius"){
            hasil = inputSuhu - 273.15;
        }else if(konversiDari == "Kelvin" && konversiKe == "Fahrenheit"){
            hasil = inputSuhu * 1.8 - 459.67;
        }else if(konversiDari == "Kelvin" && konversiKe == "Reamur"){
            hasil = (inputSuhu - 273.15) * 0.8;
        }
        
        // REAMUR
        if((konversiDari == "Reamur") && konversiKe == "Celsius"){
            hasil = inputSuhu / 0.8;
        }else if(konversiDari == "Reamur" && konversiKe == "Fahrenheit"){
            hasil = (inputSuhu - 7.5) * 24/7 + 32;
        }else if(konversiDari == "Reamur" && konversiKe == "Kelvin"){
            hasil = inputSuhu / 0.8 + 273.15;
        }
        
        return hasil;
    }

