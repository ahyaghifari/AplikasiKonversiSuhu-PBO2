    private void tfInputSuhuKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfInputSuhuKeyTyped
        char c = evt.getKeyChar();
        if(! ((Character.isDigit(c) || (c == KeyEvent.VK_BACK_SPACE) || (c == KeyEvent.VK_DELETE)))){
            getToolkit().beep();
            JOptionPane.showMessageDialog(null, "Masukkan hanya 0-9!");
            evt.consume();
        }
    }//GEN-LAST:event_tfInputSuhuKeyTyped

    private void btnConvertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConvertActionPerformed
        // ambil nilai nilai komponen input suhu, konversi dari dan tujuan konversi
        double inputSuhu = Double.parseDouble(tfInputSuhu.getText());
        String konversiDari = cbKonversiDari.getSelectedItem().toString();
        String konversiKe = cbKonversiKe.getSelectedItem().toString();
        
        // pengondisian konversi
        if(konversiDari == konversiKe){
            lblHasilInputSuhu.setText(tfInputSuhu.getText() + " " + konversiDari);
            lblHasilOutputSuhu.setText(tfInputSuhu.getText() + " " + konversiKe);
        }else{
            double hasil = convertSuhu(inputSuhu, konversiDari, konversiKe);
            lblHasilInputSuhu.setText(tfInputSuhu.getText() + " " + konversiDari);
            lblHasilOutputSuhu.setText(hasil + " " + konversiKe);
        }
        
    }//GEN-LAST:event_btnConvertActionPerformed

